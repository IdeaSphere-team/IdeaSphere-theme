document.addEventListener('DOMContentLoaded', () => {

    function updateTime() {
        const now = new Date();
        return now.toLocaleString();
    }


    // 初始化立即显示时间
    let timeString = updateTime();

    // 每秒更新一次时间（并保留定时器ID用于清理）
    const timerId = setInterval(() => {
        timeString = updateTime();
        // 新增 DOM 更新逻辑
        const usageElement = document.querySelector('#physics-usage h2');
        if (usageElement) {
            // 保留原有内容，只替换时间部分
            usageElement.innerHTML = usageElement.innerHTML.replace(/时间: .+$/, `时间: ${timeString}`);
        }
    }, 1000);

    getMachineInfo()
        .then(data => {
            const element = document.querySelector('#physics-info-area h2');
            if (element) {
                element.innerHTML = `主机名: ${data.os_name}`;
                element.innerHTML += `<br>系统:${data.os_type}`
                element.innerHTML += `<br><br>CPU架构: ${data.cpu_platform}`;
                element.innerHTML += `<br>CPU型号: ${data.cpu_model}`;
                element.innerHTML += `<br>CPU物理核心数: ${data.cpu_physics_cores_count}`;
                element.innerHTML += `<br>CPU逻辑核心数: ${data.cpu_logic_count}`;
                element.innerHTML += `<br><br>硬盘IO读入信息(单位BYTE): ${data.disk_io_read_info}`;
                element.innerHTML += `<br>硬盘IO写入信息(单位BYTE): ${data.disk_io_write_info}`;
            } else {
                console.error('元素 #physics-info-area h2 未找到');
            }
        })
        .catch(error => console.error('获取系统信息失败:', error));

    getPhysicsUsage()
        .then(data => {
            const element = document.querySelector('#physics-usage h2');
            if (element) {
                element.innerHTML = `总内存: ${data.physics_memory_total} GB`;
                element.innerHTML += `<br>物理使用量: ${data.used} GB / ${data.physics_memory_total} GB`;
                element.innerHTML += `<br>空余: ${data.available}%`;
                element.innerHTML += `<br>时间: ${timeString}`;
            } else {
                console.log('元素 #physics-usage h2 未找到')
            }
        })
        .catch(error => console.error('获取系统信息失败:', error));

    getDiskUsage()
        .then(data => {
            const diskUsageElement = document.querySelector('#disk-usage');
            if (diskUsageElement) {
                diskUsageElement.innerHTML += `<h2>使用量: ${data.disk_usage_info} GB</h2>`;
                diskUsageElement.innerHTML += `<h2>剩余量: ${data.disk_usage_info_available} GB</h2>`;

            } else {
                console.log('元素 #physics-usage h2 未找到')
            }
        })
        .catch(error => console.error('获取系统信息失败:', error));
});

